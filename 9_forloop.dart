/* For loop */

void main() {
  for (int i = 0; i < 5; i++) {
    // Inicializa 'i' como zero; enquanto 'i' for menor que 5,
    //o bloco será executado; após cada iteração, 'i' é incrementado em 1.
    print('Iteração $i');
  }

  // Após as 5 iterações, o loop 'for' é encerrado,
  //e a execução continua aqui.
  print('Loop concluído.');
}
