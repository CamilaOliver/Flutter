/* Variáveis */

void main() {
  int a = 5;
  int b = 3;

  int adicao = a + b;
  int subtracao = a - b;
  int multiplicacao = a * b;
  double divisao = a / b;
  int modulo = a % b;

  print('Adição: $adicao');
  print('Subtração: $subtracao');
  print('Multiplicação: $multiplicacao');
  print('Divisão: $divisao');
  print('Módulo: $modulo');

  /* Incremento (++) e Decremento (--): 
   Esses operadores são usados para aumentar ou diminuir o
   valor de uma variável em 1. */

  int c = 10;
  int d = 3;

  c++; // Incremento
  d--; // Decremento

  print('Incremento de c: $c');
  print('Incremento de c: $d');
}
